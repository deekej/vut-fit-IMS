int main(int argc, char* argv[])
{{{
  
  /* Any parameter used? */
  if (argc > 1) {
    display_help(argv[0]);

    if ((strcmp(argv[1],"-h") == 0) || (strcmp(argv[1], "--help") == 0)) {
      return EXIT_SUCCESS;  
    }
    
    return EXIT_FAILURE;
  }

  // // // // // // // // // // // // // // // // // // // // // // // // // // // // // // // // //

  long seed_value;
  std::ifstream seed;
  
  seed.open("/dev/urandom", std::ifstream::binary);

  seed.read(reinterpret_cast<char *>(&seed_value), sizeof(long));
  RandomSeed(seed_value);



  seed.close();

  return EXIT_SUCCESS;
}}}


